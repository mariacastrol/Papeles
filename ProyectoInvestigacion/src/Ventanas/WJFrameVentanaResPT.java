    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        jTextField1.setText(null);
        jButton2.setEnabled(false);
        if (jComboBox1.getSelectedItem().toString().startsWith("C")) {
            jButton2.setText("CREAR COPIA");
        } else {
            jButton2.setText("RESTAURAR BASE DE DATOS");
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jComboBox1.getSelectedItem().toString().startsWith("C")) {
            JFileChooser fc = new JFileChooser();
            fc.setDialogTitle("GUARDAR COPIA DE SEGURIDAD");
            fc.setMultiSelectionEnabled(false);
            int returnVal = fc.showSaveDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File sinExtension = fc.getSelectedFile();
                jTextField1.setText(sinExtension.getAbsolutePath());
                jButton2.setEnabled(true);
            } else {
                jTextField1.setText(null);
                jButton2.setEnabled(false);
            }
        } else {
            FileNameExtensionFilter filtroSQL = new FileNameExtensionFilter("Archivos SQL","sql");
            JFileChooser fc = new JFileChooser();
            fc.setDialogTitle("ABRIR COPIA DE SEGURIDAD");
            fc.setMultiSelectionEnabled(false);
            fc.setFileFilter(filtroSQL);
            fc.setApproveButtonText("Seleccionar");
            int fileValue = fc.showOpenDialog(this);
            if (fileValue == JFileChooser.APPROVE_OPTION){
                File sinExtension = fc.getSelectedFile();
                jTextField1.setText(sinExtension.getAbsolutePath());
                jButton2.setEnabled(true);
            } else {
                jTextField1.setText(null);
                jButton2.setEnabled(false);
            }
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

