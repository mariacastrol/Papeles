    public boolean setDatosConexion (String sv, String us, String pw, String dB, String [] cTM, String nTM, String pK) {
        if (ping.conectarBD(sv,us,pw,dB)) {
            columnasTablaMysql = cTM;
            nombreTablaMysql = nTM;
            this.pK = pK;
            colorFondo = jPanel1.getBackground();
            jLabelObligatorios.setForeground(colorFondo);
            if(!ping.mostrarColumnasTablaMysqlSimple(jTablePDFManifiestos, nombreTablaMysql, columnasTablaMysql)){
                JOptionPane.showMessageDialog(this, ping.getMensajesError(),"NO SE HA PODIDO CARGAR LA INFORMACIÓN DE LA TABLA",JOptionPane.ERROR_MESSAGE);
            }
            return true;
        } else {
            JOptionPane.showMessageDialog(this, ping.getMensajesError(),"NO SE HA PODIDO CONECTAR A LA BASE",JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }
    
    private void funcionSeleccionar() {
        int filaSeleccionada = jTablePDFManifiestos.getSelectedRow();
        if (filaSeleccionada >= 0) {
            String celda = jTablePDFManifiestos.getValueAt(filaSeleccionada,0).toString();
            String celda1 = jTablePDFManifiestos.getValueAt(filaSeleccionada,1).toString();
            jTextFieldSRutaPDF.setText(celda);
            jTextFieldSNombrePDF.setText(celda1);       
            jLabel21.setForeground(new java.awt.Color(0,0,0));
            jLabel22.setForeground(new java.awt.Color(0,0,0));
        } else {
            JOptionPane.showMessageDialog(this,mensajeNoSeleccionado,"SELECCIÓN",JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void funcionVolver() {
        if (FuncionesGenerales.estaVacioJTextField(jTextFieldSRutaPDF)) {
            AJFrameVentanaCapturas.jLabel39.setEnabled(false);
            AJFrameVentanaCapturas.rutaPDF = null;
        } else {
            AJFrameVentanaCapturas.jLabel39.setEnabled(true);
            AJFrameVentanaCapturas.rutaPDF = jTextFieldSRutaPDF.getText();
        }
        this.dispose(); 
    }

    private boolean estanVacios() {
        if (FuncionesGenerales.estaVacioJTextField(jTextFieldAARutaPDF)) {
            botonPDF.setForeground(new java.awt.Color(rError,gError,bError));
            jLabelObligatorios.setForeground(new java.awt.Color(rError,gError,bError));
            return true;
        }
        return false;
    }
    
    private void limpiarAA() {
        jTextFieldAARutaPDF.setText(null);
        jTextFieldAANombrePDF.setText(null);
        jLabelObligatorios.setForeground(colorFondo);
        botonPDF.setForeground(new java.awt.Color(0,0,0));
    }
  
